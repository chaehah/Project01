package com.kjca.dto;

public class ManagerDTO {
	private int managerNo;
	private String managerId;
	private String managerPw;
	private String managerName;
	private String managerEmail;
	private String managerPhone;
	private int managerFlag;
	private int managerAdminFlag;
	
	public int getManagerNo() {
		return managerNo;
	}
	public void setManagerNo(int managerNo) {
		this.managerNo = managerNo;
	}
	public String getManagerId() {
		return managerId;
	}
	public void setManagerId(String managerId) {
		this.managerId = managerId;
	}
	public String getManagerPw() {
		return managerPw;
	}
	public void setManagerPw(String managerPw) {
		this.managerPw = managerPw;
	}
	public String getManagerName() {
		return managerName;
	}
	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}
	public String getManagerEmail() {
		return managerEmail;
	}
	public void setManagerEmail(String managerEmail) {
		this.managerEmail = managerEmail;
	}
	public String getManagerPhone() {
		return managerPhone;
	}
	public void setManagerPhone(String managerPhone) {
		this.managerPhone = managerPhone;
	}
	public int getManagerFlag() {
		return managerFlag;
	}
	public void setManagerFlag(int managerFlag) {
		this.managerFlag = managerFlag;
	}
	public int getManagerAdminFlag() {
		return managerAdminFlag;
	}
	public void setManagerAdminFlag(int managerAdminFlag) {
		this.managerAdminFlag = managerAdminFlag;
	}
	
}
